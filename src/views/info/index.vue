      });
      this.info = res;
    },
    // 收藏
    collected() {
      addcollect({
        goodsId: this.$route.query.id,
        openId: localStorage.getItem("openId"),
      }).then((res) => {
        console.log(res);
        //👉当接口调取成功之后
        //需初始化 再获取页面最新的数据
        this.init();
      });
    },
    //控制弹出层
