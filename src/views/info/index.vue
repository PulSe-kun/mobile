      //2.添加购物车
      if (this.show) {
        await addCart({
          goodsId: this.$route.query.id,
          number: this.value,
          openId: localStorage.getItem("openId"),
        });
        await this.init();
        Toast({
          type: "success",
          message: "添加成功",
          duration: 2000,
        });
      } else {
        this.show = !this.show;
      }
    },
    refresh(id) {
      // 4.再调取接口
      detailaction({
        id: id,
        openId: localStorage.getItem("openId"),
      }).then((res) => {
        console.log(res);
        this.info = res;
      });
